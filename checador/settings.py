def get_env(key, default=None, cast=None):
    value = os.environ.get(key, default)
    if cast and value is not None:
        if cast == bool:
            return value.lower() in ['true', '1', 'yes']
        elif cast == list:
            return [item.strip() for item in value.split(',')]
        else:
            return cast(value)
    return value

